namespace Webkul\MpZipCodeValidator\Block\Adminhtml\Region;

use Magento\Backend\Block\Widget\Form\Container;

class Edit extends Container
{
    /**
     * Core registry
     *
     * @var \Magento\Framework\Registry
     */
    protected $_coreRegistry;

    /**
     * @param \Magento\Backend\Block\Widget\Context $context
     * @param \Magento\Framework\Registry $registry
     * @param array $data
     */
    public function __construct(
        \Magento\Backend\Block\Widget\Context $context,
        \Magento\Framework\Registry $registry,
        array $data = []
    ) {
        $this->_coreRegistry = $registry;
        parent::__construct($context, $data);
    }

    /**
     * Department edit block
     *
     * @return void
     */
    protected function _construct()
    {
        $this->_objectId = 'id';
        $this->_blockGroup = 'Webkul_MpZipCodeValidator';
        $this->_controller = 'adminhtml_region';

        parent::_construct();

        if ($this->_isAllowedAction('Webkul_MpZipCodeValidator::save')) {
            $this->buttonList->update('save', 'label', __('Save'));
        }
    }

    /**
     * Get header with Department name
     *
     * @return \Magento\Framework\Phrase
     */
    public function getHeaderText()
    {
        if ($this->_coreRegistry->registry('mpzipcodevalidator')->getId()) {
            return __("Edit Region '%1'", $this->escapeHtml(
                $this->_coreRegistry->registry('mpzipcodevalidator')->getTypeCode()
            ));
        } else {
            return __('New Region');
        }
    }

    /**
     * Check permission for passed action
     *
     * @param string $resourceId
     * @return bool
     */
    protected function _isAllowedAction($resourceId)
    {
        return $this->_authorization->isAllowed($resourceId);
    }
}
